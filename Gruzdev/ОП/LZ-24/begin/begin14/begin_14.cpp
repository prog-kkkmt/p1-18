#include <iostream>
using namespace std;

int main(){
const double Pi = 3.14;
double r, s, l;
	cin >> l;
    r = l/(Pi * 2);
    s = Pi * r * r;
	cout << "Радиус окружности = " << r << endl
	<< "Площадь круга = " << s << endl;
return 0;
}

