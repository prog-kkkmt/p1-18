#include <iostream>
#include <vector>

#include "remove_for_inc.h"

using namespace std;

int main(){
	setlocale(LC_ALL, "rus");
	cout << "Введите кол-во элементов в векторе: ";
	size_t vec_size;
	cin >> vec_size;

	cout << "Введите вещественные элементы вектора:" << endl;
	vector <double> vec;
	double elem;
	for (size_t i = 0; i != vec_size; ++i){
		cin >> elem;
		vec.push_back(elem);
	}
		
	RemoveForInc(&vec, vec_size);

	vec_size = vec.size();
	cout << "Output:" << endl;
	for (size_t i = 0; i != vec_size; ++i)
		cout << vec[i] << endl;

	return 0;
}
